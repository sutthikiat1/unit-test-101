// การระบุประเภทชนิด type ตาม index ใน array
const point: [number, number] = [10, 20];

console.log(point[10]); // error เพราะไม่มีการ infer type ช่องที่ 10

point.push(30); // ไม่ error เพราะ ถึงแม้เราจะเพิ่มค่าอะไรลงไปก็ได้ แต่ถ้าหากเรากำหนดค่าไว้แค่ 2 index เราจะสามารถอ่านค่าได้แค่ 2 index มันไม่ได้ป้องกันการเพิ่มค่า แต่มันป้องกันการอ่านค่า
